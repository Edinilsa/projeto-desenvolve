# ---------------------------------------
# Título: Primeiros comandos com print() e type()
# ---------------------------------------

# 🧠 O comando print() serve para mostrar algo na tela (saída do programa)

print("Olá, mundo!")  # Frase clássica de início

# Podemos imprimir números, textos, contas e variáveis
print(10)
print("A soma de 2 + 3 é:", 2 + 3)

# Podemos usar aspas simples ou duplas para strings
print('Python é divertido!')
print("Também funciona com aspas duplas.")

# ---------------------------------------

# 🧪 O comando type() mostra o tipo do dado (int, float, str, etc.)

print(type(10))           # int (número inteiro)
print(type(3.14))         # float (número decimal)
print(type("Python"))     # str (texto/string)

# ---------------------------------------

# 🧩 Agora combinando: variáveis + print + type

nome = "Maria"
idade = 28
altura = 1.65

print("Nome:", nome)
print("Idade:", idade)
print("Altura:", altura)

print("Tipo de nome:", type(nome))      # str
print("Tipo de idade:", type(idade))    # int
print("Tipo de altura:", type(altura))  # float

